package com.learn.java.basics;

public class AbbreviatingOperators {
    public static void main(String[] args) {
        int value = 3;
        int result = 1 + value; //4
        System.out.println("result = " + result);
        result += 2; //6
        System.out.println("result = " + result);
        result -= 1; //5
        System.out.println("result = " + result);
        result *= 10; //50
        System.out.println("result = " + result);
        result /= 2; //25
        System.out.println("result = " + result);

    }
}
